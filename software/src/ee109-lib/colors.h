#ifndef __COLORS_H__
#define __COLORS_H__

/* 16-bit RGB color values. */
#define BLACK           0x0000     
#define NAVY            0x000F     
#define DARK_GREEN      0x03E0     
#define DARK_CYAN       0x03EF
#define MAROON          0x7800    
#define PURPLE          0x780F    
#define OLIVE           0x7BE0     
#define LIGHT_GREY      0xC618     
#define DARK_GREY       0x7BEF     
#define BLUE            0x001F     
#define GREEN           0x07E0     
#define CYAN            0x07FF    
#define RED             0xF800   
#define MAGENTA         0xF81F    
#define YELLOW          0xFFE0    
#define WHITE           0xFFFF    
#define ORANGE          0xFD20    
#define GREEN_YELLOW    0xAFE5 
#define PINK            0xF81F

#endif /* __COLORS_H__ */
